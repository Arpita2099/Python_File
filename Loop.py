# For single parameter in range.
for n in range(5):
    print(n)
print()

# For two parameter in range.
for a in range(1,7):
    print(a)
print()

# For three parameter in range.
for n in range(1,9,2):
    print(n)
print()

for a in range(1,11):
    print("2*",a,"=",2*a)
print()
