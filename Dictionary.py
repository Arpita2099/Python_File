# Dictionary is an unordered collection of key_value pairs.
# In the python, dictionary ids defined within the {} with each item being a pair in the form of key:value.

d={"Name":"Arpita",
  'Last_name':"Sah"
  }
print(d) # It will give output: {'Name': 'Arpita', 'Last_name': 'Sah'} 
print(type(d)) # It will give output: <class 'dict'>
